package JavaCode;

public class switchStatements {

	public static void main(String[] args) {
		int num=3;
		//String name=10;
		switch (num)
		{
		case 0:
			System.out.println(":"+num);
		    break;
		case 1:
			System.out.println("number is:"+num);
			break;
		case 2:
			System.out.println("number is:"+num);
			break;
		case 3:
			System.out.println("number is:"+num);
		    break;
		    
		default:
			System.out.println("Default number is:"+num);
		}

	}

}
